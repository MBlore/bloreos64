#ifndef _BLOREOS_GDT_H
#define _BLOREOS_GDT_H

#include <stdint.h>
#include <stddef.h>

struct gdt_entry
{
    uint16_t limit_low;
    uint16_t base_low;
    uint8_t base_middle;
    uint8_t access;
    uint8_t granularity;
    uint8_t base_high;
} __attribute__((packed));

struct gdt_ptr
{
    uint16_t limit;
    uint16_t base;
} __attribute__((packed));

#define GDT_ENTRIES 3

void init_gdt();

#endif